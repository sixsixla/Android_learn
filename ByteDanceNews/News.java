package hdu.course.android.bytedancenews;

public class News {

    private String title;
    private String image;

    public void setTitle(String title) {
        this.title = title;
    }
    public String getTitle() {
        return this.title;
    }

    public void setImage(String thumb) {
        this.image = thumb;
    }
    public String getImage() {
        return this.image;
    }

}

